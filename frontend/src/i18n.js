export const dictionaries = { en, es }

export function makeT(lang) {
  const dict = dictionaries[lang] || en
  return (key, vars) => {
    let s = dict[key] ?? en[key] ?? key
    if (vars) for (const k of Object.keys(vars)) s = s.replaceAll(`{${k}}`, vars[k])
    return s
  }
}